    public void actualizar(int op) {
        CB_JD1_Armas.removeAllItems();
        switch (op) {
            case 1:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Ruso")) {
                        CB_JD1_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                break;
            case 2:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Aleman")) {
                        CB_JD1_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                break;
            case 3:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Alumno")) {
                        CB_JD1_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                break;
        }
    }

    public void actualizar2(int op) {
        CB_JD2_Armas.removeAllItems();
        CB_JD2_Soldados.removeAllItems();
        switch (op) {
            case 1:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Ruso")) {
                        CB_JD2_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                for (int j = 0; j < listRusos.size(); j++) {
                    CB_JD2_Soldados.addItem(listRusos.get(j).getID() + "");
                }
                break;
            case 2:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Aleman")) {
                        CB_JD2_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                for (int j = 0; j < listRusos.size(); j++) {
                    CB_JD2_Soldados.addItem(listAlemanes.get(j).getAlias());
                }
                break;
            case 3:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Alumno")) {
                        CB_JD2_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                for (int j = 0; j < listRusos.size(); j++) {
                    CB_JD2_Soldados.addItem(listAlumnos.get(j).getApodo());
                }
                break;
        }
    }

